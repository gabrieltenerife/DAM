    private void tablaComponentAdded(java.awt.event.ContainerEvent evt) {//GEN-FIRST:event_tablaComponentAdded
        DefaultTableModel modeloDefault = new DefaultTableModel(new String []{"Animal", "Numero", "Poduccion", "Comentarios"},TablaJava.animales.size());
        tabla.setModel(modeloDefault);
        
        TableModel modeloDatos = tabla.getModel();
        for(int i = 0; i< TablaJava.animales.size(); i++){
        Animal animal = TablaJava.animales.get(i);
        modeloDatos.setValueAt(animal.getTipo(),i,0);
        modeloDatos.setValueAt(animal.getCodigo(),i,1);
        modeloDatos.setValueAt(animal.getEstadovacuna(),i,2);
        modeloDatos.setValueAt(animal.getProduccion(),i,3);
        modeloDatos.setValueAt(animal.getComentario(),i,4);
        }
    }//GEN-LAST:event_tablaComponentAdded

   

